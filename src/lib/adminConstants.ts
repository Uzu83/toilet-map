// WHY 独立ファイル: admin/page.tsx(Server Component)と api/admin/reviews/route.ts の両方が
// 同じ値を宣言していた。片方だけ変えると「初期表示の件数と再取得 API の件数がズレる」沈黙バグが
// 起きる(ロードしてから編集→リフレッシュで行数が変わる、等)。単一の真実として切り出す。

// 一覧に出すコメント付きレビューの最大件数。
// ───────────────────────────────────────────────────────────────────
// なぜ「100」か(無制限でも小さくもなく):
//   上限なし: コメント件数が将来数千になるとレスポンス肥大 + 関連 toilets .in() が巨大化 → admin の応答性が落ちる。
//   小さすぎ(例 10): ソロ運営が 1 セッションで「未反映の有用コメント」を取りこぼす。
//   100 ≒ 「1 セッションで目視レビューして編集に回せる現実的な上限」。
//   これを超えて溜まったら ページャ追加よりも「AI 抽出で半自動化(Phase B)」で捌く設計なので際限なく増やさない。
//   ⚠️ admin/page.tsx の Server Component 初期表示と api/admin/reviews のクライアント再取得 API で
//   この値が一致していないと、編集後リフレッシュで一覧行数が変わり運営が混乱する。
//   PostgREST の 1 レスポンス 1000 行上限以内なので内部ページングは不要。
// ───────────────────────────────────────────────────────────────────
export const ADMIN_MAX_REVIEWS = 100;

// OSM `opening_hours` 文字列の「24時間営業か?」だけを判定する純関数。
//
// WHY (なぜフル構文パーサを使わないのか):
//   package.json には既に `opening_hours` 依存がある(seed/OSM タグパース用)が、
//   それをこの判定のためにクライアント(PinSheet)バンドルへ持ち込むのは割に合わない。
//   - フル構文パーサはタイムゾーン・祝日(PH)・例外日・季節差を解釈するため重く、
//     バッジ 1 個のためにクライアントバンドルを膨らませる。
//   - 「いま営業中か」のリアルタイム判定は TZ/祝日エッジが多く、設計書でも今回スコープ外と決定済み。
//   よって、確実に常時開放と分かる正準形だけを小さな文字列一致で拾い、それ以外は false に倒す。
//
// WHY (なぜ完全一致 `=== "24/7"` のみ true なのか / why-not 接尾辞付きを true にしないのか):
//   OSM では `24/7; PH off`(祝日は休業)や `24/7 open` のような接尾辞・例外付きの値が存在する。
//   これらを true にして「24時間」バッジだけ出すと、祝日休業などの例外情報が UI から消える
//   false positive になり、利用者を締め出された場所へ誘導しかねない。
//   そこで接尾辞・例外が一切付かない正準形 `24/7` のみ true とし、それ以外(接尾辞付き含む)は
//   false を返す。false のとき呼び出し側(PinSheet)は opening_hours の生文字列を営業時間行に
//   そのまま表示するので、例外情報を隠さずに済む = 安全側に倒す設計。
export function is24h(raw: string | null | undefined): boolean {
  // null / undefined / 空文字は当然 24h ではない。
  // trim() で前後空白を除去('  24/7  ' のような入力も正準形として拾う)、
  // toLowerCase() は将来 '24/7 OPEN' のような大文字混じりを正規化するため
  // (現状 '24/7' に英字は無いが、比較を堅牢にしておく)。
  if (!raw) return false;
  return raw.trim().toLowerCase() === "24/7";
}
